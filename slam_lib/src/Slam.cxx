//-----------------------------------------------------------------------------
void Slam::ClearLog()
{
  auto prevlastLogStateIt = LogStates.end();
  --prevlastLogStateIt;
  --prevlastLogStateIt;
  std::list<LidarState> storeLog;
  storeLog.push_back(*prevlastLogStateIt);
  storeLog.push_back(*(++prevlastLogStateIt));

  this->LogStates.clear();
  this->LogStates = storeLog;
}

